
/**
 * Sample usage of "this" keyword.
 * 
 * @author Reyes, Omar Matthew B. 
 * @version 2013/09/09
 */
public class ThisSample
{
    private int x, y;
    public void Assign(int x, int y){
        this.x = x;
        this.y = y;
    }
    public int getX(){
        return x;
    }
    public int getY(){
        return y;
    }
}
